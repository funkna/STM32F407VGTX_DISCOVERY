// Redundancy Prevention --------------------------------------------------------------------------
#ifndef LED_H_
#define LED_H_

// Includes ---------------------------------------------------------------------------------------
#include "types.h"

// Defines ----------------------------------------------------------------------------------------
// Typedefs ---------------------------------------------------------------------------------------

// -------------------------------------------------------------
// LED type enumerations.
// -------------------------------------------------------------
typedef enum
{
   LED_GREEN,
   LED_ORANGE,
   LED_RED,
   LED_BLUE
} LEDTypeEnum;

// Functions --------------------------------------------------------------------------------------

// -------------------------------------------------------------
// Inititalize the LED GPIO pins.
// -------------------------------------------------------------
BOOL LED_Initialize();

// -------------------------------------------------------------
// Turn on an LED.
// -------------------------------------------------------------
void LED_On(
   LEDTypeEnum eLED_);

// -------------------------------------------------------------
// Turn off an LED.
// -------------------------------------------------------------
void LED_Off(
   LEDTypeEnum eLED_);

// -------------------------------------------------------------
// Toggle an LED.
// -------------------------------------------------------------
void LED_Toggle(
   LEDTypeEnum eLED_);

#endif // LED_H_
